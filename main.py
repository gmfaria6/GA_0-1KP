def validate_cromo(croma, weights, capacities):
    current_weight_slots = []
    selected_items = []
    for slot in range(0, len(capacities)):
        current_weight = 0
        item = 0
        for bit in croma:
            if bit == '1':
                current_weight += weights[slot][item]
                selected_items.append(item)
            item += 1
        while current_weight > capacities[slot]:
            random_item = random.choice(selected_items)
            selected_items.remove(random_item)
            if croma[random_item] == '1':
                croma = croma[0:random_item] + '0' + croma[random_item + 1:len(weights[0])]
                current_weight -= weights[slot][random_item]
        current_weight_slots.append(current_weight)

    item = 0

    items = [i for i in range(0, len(weights[0]))]
    random.shuffle(items)

    for bit in items:
        if croma[bit] == '0':
            flag = False
            for slot in range(0, len(capacities)):
                if current_weight_slots[slot] + weights[slot][bit] <= capacities[slot]:
                    flag = True
                else:
                    flag = False
                    break
            if flag:
                croma = croma[0:bit] + '1' + croma[bit + 1:len(weights[0])]
                for slot in range(0, len(capacities)):
                    current_weight_slots[slot] += weights[slot][bit]
    return croma


